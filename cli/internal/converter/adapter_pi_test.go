func testdataPath(parts ...string) string {
	_, filename, _, _ := runtime.Caller(0)
	dir := filepath.Dir(filename)
	return filepath.Join(append([]string{dir, "testdata"}, parts...)...)
}

func TestPiAdapterEncode_Basic(t *testing.T) {
	hooks := &CanonicalHooks{
		Spec: SpecVersion,
		Hooks: []CanonicalHook{
			{
				Name:     "test-hook",
				Event:    "before_tool_execute",
				Blocking: true,
				Handler:  HookHandler{Type: "command", Command: "echo check"},
			},
		},
	}

	adapter := AdapterFor("pi")
	encoded, err := adapter.Encode(hooks)
	if err != nil {
		t.Fatalf("Encode: %v", err)
	}

	out := string(encoded.Content)
	assertContains(t, out, "Generated by syllago")
	assertContains(t, out, "tool_call") // before_tool_execute → tool_call
	assertContains(t, out, "echo check")
	assertEqual(t, "syllago-hooks.ts", encoded.Filename)
}

