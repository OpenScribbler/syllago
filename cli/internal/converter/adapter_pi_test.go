func TestPiAdapterEncode_CommandInjectionSafe(t *testing.T) {
	hooks := &CanonicalHooks{
		Spec: SpecVersion,
		Hooks: []CanonicalHook{
			{
				Event:   "session_start",
				Handler: HookHandler{Type: "command", Command: `"); process.exit(1); ("`},
			},
		},
	}

	adapter := AdapterFor("pi")
	encoded, err := adapter.Encode(hooks)
	if err != nil {
		t.Fatalf("Encode: %v", err)
	}

	out := string(encoded.Content)
	// The malicious command is JSON-escaped — quotes backslash-escaped, safe as string literal
	assertContains(t, out, `\"); process.exit(1); (\"`)
	// Verify there's only ONE child process call — no injection created a second
	if strings.Count(out, "Sync(") != 1 {
		t.Errorf("expected exactly 1 child process call, got %d — possible injection", strings.Count(out, "Sync("))
	}
}

func TestPiAdapterEncode_UnsupportedEventDropped(t *testing.T) {
	hooks := &CanonicalHooks{
		Spec: SpecVersion,
		Hooks: []CanonicalHook{
			{
				Event:   "worktree_create",
				Handler: HookHandler{Type: "command", Command: "echo wt"},
			},
		},
	}

	adapter := AdapterFor("pi")
	encoded, err := adapter.Encode(hooks)
	if err != nil {
		t.Fatalf("Encode: %v", err)
	}

	hasWarning := false
	for _, w := range encoded.Warnings {
		if strings.Contains(w.Description, "not supported by pi") {
			hasWarning = true
			break
		}
	}
	if !hasWarning {
		t.Error("expected warning for unsupported event")
	}
}

func TestPiAdapterDecode_MarkerBased(t *testing.T) {
	code, err := os.ReadFile(testdataPath("pi", "generated-simple.ts"))
	if err != nil {
		t.Fatalf("read testdata: %v", err)
	}

	adapter := AdapterFor("pi")
	hooks, err := adapter.Decode(code)
	if err != nil {
		t.Fatalf("Decode: %v", err)
	}

	if len(hooks.Hooks) < 1 {
		t.Fatalf("expected at least 1 hook, got %d", len(hooks.Hooks))
	}

	h := hooks.Hooks[0]
	assertEqual(t, "safety-check", h.Name)
	assertEqual(t, "before_tool_execute", h.Event) // tool_call → before_tool_execute
	if !h.Blocking {
		t.Error("expected blocking to be true")
	}
	if h.Handler.Timeout != 5 {
		t.Errorf("expected timeout 5s, got %d", h.Handler.Timeout)
	}
}

