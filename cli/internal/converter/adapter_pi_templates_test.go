func TestRenderPiExtension_CommandInjectionInTemplate(t *testing.T) {
	data := piExtensionTemplateData{
		Hooks: []piHookTemplateData{
			{PiEvent: "tool_call", Command: `"); process.exit(1); ("`, Blocking: false},
		},
	}

	out, err := renderPiExtension(data)
	if err != nil {
		t.Fatalf("renderPiExtension: %v", err)
	}

	s := string(out)
	// The malicious command must be JSON-escaped: quotes are backslash-escaped,
	// so the string literal can't break out of the argument.
	assertContains(t, s, `\"); process.exit(1); (\"`)
	// Verify there's only ONE child process call — no injection created a second
	if strings.Count(s, "Sync(") != 1 {
		t.Errorf("expected exactly 1 child process call, got %d — possible injection", strings.Count(s, "Sync("))
	}
}
